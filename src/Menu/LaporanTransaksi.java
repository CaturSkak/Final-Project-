    public void getSum()
    {
        int sum = 0;
        for(int i = 0; i < TabelPesanan.getRowCount(); i++)
        {
            sum = sum + Integer.parseInt(TabelPesanan.getValueAt(i, 4).toString());
        }
        
        Total.setText(Integer.toString(sum));
    }
    
    public void tampil(){
         try {
            Koneksi ObjKoneksi = new Koneksi();
            Connection con = ObjKoneksi.bukaKoneksi();
            Statement st = con.createStatement();
            String sql = "SELECT * FROM laporan";
            ResultSet set = st.executeQuery(sql);
            //menampilkan data ke Tabel
            ResultSetTableModel model = new ResultSetTableModel(set);
            TabelPesanan.setModel(model);
            
        } catch (SQLException e) {
            System.out.println("gagal query");
        }
        int b=TabelPesanan.getRowCount();
        number.setText(""+b);
    }
